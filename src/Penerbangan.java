    private void CariData(){
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM penerbangan WHERE " 
                                    + cmbcari.getSelectedItem().toString() +
                                    " LIKE '%" +tfcari.getText() + "%'");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("ID Penerbangan");
            model.addColumn("Maskapai");
            model.addColumn("Keberangkatan");
            model.addColumn("Kota Tujuan");
            model.addColumn("Tgl Berangkat");
            model.addColumn("Harga");
            
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()){
                Object[] data = {
                    no ++,
                    rs.getString("id_penerbangan"),
                    rs.getString("nama_penerbangan"),
                    rs.getString("kota_keberangkatan"),
                    rs.getString("kota_tujuan"),
                    rs.getString("tanggal_keberangkatan"),
                    rs.getString("harga"),
                };
                model.addRow(data);
                jTable2.setModel(model);                               
            }        
            
        }catch (Exception e){            
        }
    }
    
