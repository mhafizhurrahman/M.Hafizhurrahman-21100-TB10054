    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        try {
            st = cn.createStatement();
            if (tfidpene.getText().equals("") || 
                tfnama.getText().equals("") ||
                tfkebe.getText().equals("") ||                    
                tftuju.getText().equals("") ||
                tftgl.getText().equals("") ||
                tfHarga.getText().equals("")){  
                JOptionPane.showMessageDialog(null,"data tidak boleh kosong","validasi Data",JOptionPane.INFORMATION_MESSAGE);
                return;              
                
               
            }
            //fungsi simpan
            if (btsimpan.getText()== "Simpan") { 
                String cek = "SELECT * FROM penerbangan WHERE id_penerbangan  = '" + tfidpene.getText() + "'";
                rs = st.executeQuery(cek);
                if (rs.next()){
                    JOptionPane.showMessageDialog(null, "ID Sudah Ada");
                }else{
                    String sql = "INSERT INTO penerbangan VALUES ('" + tfidpene.getText()+
                            "','" + tfnama.getText() +
                            "','" + tfkebe.getText() +
                            "','" + tftuju.getText() +
                            "','" + tftgl.getText() +
                            "','" + tfHarga.getText() + "')";
                    
                    
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil Disimpan");
                    TampilData();
                    Bersih();
                    
                }
                
            }else{
             //ubah / edit data  
             String update = "UPDATE penerbangan SET nama_penerbangan = '" + tfnama.getText()+
                                                          "',kota_keberangkatan = '" + tfkebe.getText()+
                                                          "',kota_tujuan = '" + tftuju.getText()+ 
                                                          "',tanggal_keberangkatan = '" + tftgl.getText()+
                                                          "',harga = '" + tfHarga.getText()+                     
                                                          "' WHERE id_penerbangan = '" + tfidpene.getText()+"'";
             st.executeUpdate(update);
             JOptionPane.showMessageDialog(null,"Data Berhasil di ubah");
             TampilData();
             Bersih();
            }
          }catch(Exception e){
              JOptionPane.showMessageDialog(null,e);
              
          }// TODO add your handling code here:
    }//GEN-LAST:event_btsimpanActionPerformed

    private void bthapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthapusActionPerformed
       //koding hapus
        if (tfidpene.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Silahkan pilih data dahulu");
        }else{
            int jawab = JOptionPane.showConfirmDialog(null,"Data ini akan dihapus, anda yakin untuk melanjutkan ?","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if (jawab == 0) {
                try {
                    st = cn.createStatement();
                    String sql = "DELETE FROM penerbangan WHERE id_penerbangan = '" + tfidpene.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "data berhasil dihapus");
                    TampilData();
                    Bersih();
                    
                }catch (Exception e){
                  JOptionPane.showMessageDialog(null,e); 
                }
            }
        }        
    }//GEN-LAST:event_bthapusActionPerformed

